oFF.InARuntimeUtils = {

	_addRuntimeCapability:function(inaContext)
	{
			let capabilities = inaContext.getListByKey("Capabilities");
		capabilities.addString(oFF.InACapabilities.V266_RESULTSETV2_METADATA_EXTENSION1);
	},
	getDataSourceFromRequest:function(request)
	{
			let context = oFF.InARuntimeUtils.getRequestContext(request);
		if (oFF.isNull(context))
		{
			let definition = request.getStructureByKey("Definition");
			if (oFF.isNull(definition))
			{
				return null;
			}
			return definition.getStructureByKey("DataSource");
		}
		return context.getStructureByKey("DataSource");
	},
	getDimensionsListFromRequest:function(request)
	{
			if (oFF.isNull(request))
		{
			return null;
		}
		let inaDefinition = null;
		let inaContext = oFF.InARuntimeUtils.getRequestContext(request);
		if (oFF.isNull(inaContext))
		{
			inaDefinition = request.getStructureByKey("Definition");
			if (oFF.isNull(inaDefinition))
			{
				return null;
			}
		}
		else
		{
			inaDefinition = inaContext.getStructureByKey("Definition");
		}
		if (oFF.isNull(inaDefinition))
		{
			return null;
		}
		return inaDefinition.getListByKey("Dimensions");
	},
	getHashableRequest:function(context)
	{
			let hashAbleRequest = oFF.PrFactory.createStructure();
		let dataSource = context.getStructureByKey("DataSource");
		dataSource.remove("InstanceId");
		hashAbleRequest.put("DataSource", dataSource);
		let definition = context.getStructureByKey("Definition");
		definition.remove("Name");
		let definitionKeys = definition.getKeysAsIterator();
		while (definitionKeys.hasNext())
		{
			let definitionKey = definitionKeys.next();
			if (!oFF.XString.isEqual(definitionKey, "ResultSetFeatureRequest"))
			{
				hashAbleRequest.put(definitionKey, definition.getByKey(definitionKey));
			}
		}
		return oFF.PrUtils.serialize(hashAbleRequest, true, false, 0);
	},
	getObjectNameFromDimensionDataSource:function(dimension)
	{
			if (oFF.isNull(dimension))
		{
			return null;
		}
		let dataSource = dimension.getStructureByKey("DataSource");
		if (oFF.isNull(dataSource))
		{
			return null;
		}
		return dataSource.getStringByKey("ObjectName");
	},
	getQueryNameFromContext:function(context)
	{
			if (oFF.isNull(context))
		{
			return null;
		}
		let definition = context.getStructureByKey("Definition");
		return definition.getStringByKey("Name");
	},
	getRequestContext:function(inaRequest)
	{
			let context = inaRequest.getStructureByKey("Analytics");
		if (oFF.notNull(context))
		{
			return context;
		}
		context = inaRequest.getStructureByKey("Planning");
		if (oFF.notNull(context))
		{
			return context;
		}
		context = inaRequest.getStructureByKey("ListReporting");
		if (oFF.notNull(context))
		{
			return context;
		}
		return null;
	},
	getSystemNameFromContext:function(context)
	{
			if (oFF.isNull(context))
		{
			return null;
		}
		let dataSource = context.getStructureByKey("DataSource");
		if (oFF.isNull(dataSource))
		{
			return null;
		}
		return dataSource.getStringByKey("System");
	},
	getSystemNameFromRequest:function(request)
	{
			let context = oFF.InARuntimeUtils.getRequestContext(request);
		return oFF.InARuntimeUtils.getSystemNameFromContext(context);
	},
	setLanguage:function(request, language)
	{
			if (oFF.isNull(language))
		{
			return;
		}
		let batch = request.getListByKey(oFF.ConnectionConstants.INA_BATCH);
		if (!oFF.PrUtils.isListEmpty(batch))
		{
			let numberOfRequestsInBatch = batch.size();
			for (let i = 0; i < numberOfRequestsInBatch; i++)
			{
				let batchContext = oFF.InARuntimeUtils.getRequestContext(batch.getStructureAt(i));
				batchContext.putString("Language", language);
			}
		}
		else
		{
			let context = oFF.InARuntimeUtils.getRequestContext(request);
			context.putString("Language", language);
		}
	},
	setLanguageAndAddRuntimeCapability:function(request, language)
	{
			let batch = request.getListByKey(oFF.ConnectionConstants.INA_BATCH);
		if (!oFF.PrUtils.isListEmpty(batch))
		{
			let numberOfRequestsInBatch = batch.size();
			for (let i = 0; i < numberOfRequestsInBatch; i++)
			{
				let batchContext = oFF.InARuntimeUtils.getRequestContext(batch.getStructureAt(i));
				oFF.InARuntimeUtils._addRuntimeCapability(batchContext);
				batchContext.putString("Language", language);
			}
		}
		else
		{
			let context = oFF.InARuntimeUtils.getRequestContext(request);
			oFF.InARuntimeUtils._addRuntimeCapability(context);
			context.putString("Language", language);
		}
	},
	setPersistencyForLocal:function(inaDataSource, remoteQueryIdentifier, systemMapping)
	{
			inaDataSource.putString("InstanceId", remoteQueryIdentifier);
		inaDataSource.putString("ObjectName", systemMapping.getDeserializeTable());
		inaDataSource.remove("PackageName");
		inaDataSource.putString("SchemaName", systemMapping.getDeserializeSchema());
		inaDataSource.putString("Type", "SerializedData");
	},
	setPersistencyForRemote:function(context, systemMapping)
	{
			let definition = context.getStructureByKey("Definition");
		let rsFeature = definition.getStructureByKey("ResultSetFeatureRequest");
		rsFeature.putString("ResultFormat", "SerializedData");
		if (oFF.isNull(systemMapping))
		{
			rsFeature.remove("ResultSetPersistanceSchema");
			rsFeature.remove("ResultSetPersistanceTable");
		}
		else
		{
			rsFeature.putString("ResultSetPersistanceSchema", systemMapping.getSerializeSchema());
			rsFeature.putString("ResultSetPersistanceTable", systemMapping.getSerializeTable());
		}
		rsFeature.remove("ReturnEmptyJsonResultSet");
	},
	setPersistencyIdentifier:function(context, runtimeRemoteIdentifier)
	{
			let definition = context.getStructureByKey("Definition");
		let rsFeature = definition.getStructureByKey("ResultSetFeatureRequest");
		rsFeature.putString("ResultSetPersistanceIdentifier", runtimeRemoteIdentifier);
	}
};

