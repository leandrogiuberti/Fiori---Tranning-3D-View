oFF.SleMetis.initUserSpecifiedSystems = function(application)
{
	oFF.SleMetis.s_userSpecifiedSystems = null;
	if (oFF.isNull(oFF.SleMetis.s_userSpecifiedSystems))
	{
		oFF.SleMetis.s_userSpecifiedSystems = oFF.XList.create();
		let userSpecifiedSystems = application.getProcess().getLocalStorage().getStringByKeyExt(oFF.SleMetis.USER_SPECIFIED_SYSTEMS_KEY, "");
		let serializedSystemDescs = oFF.XStringTokenizer.splitString(userSpecifiedSystems, oFF.SleMetis.METIS_SYSTEM_SEPARATOR);
		if (oFF.notNull(serializedSystemDescs))
		{
			for (let i = 0; i < serializedSystemDescs.size(); i++)
			{
				let serializedSysDesc = serializedSystemDescs.get(i);
				if (oFF.XStringUtils.isNotNullAndNotEmpty(serializedSysDesc))
				{
					let tmpSysProps = oFF.XProperties.create();
					tmpSysProps.deserialize(serializedSysDesc);
					let newSysDesc = oFF.SystemDescription.create(application.getSystemLandscape(), null, tmpSysProps);
					oFF.SleMetis.s_userSpecifiedSystems.add(newSysDesc);
				}
			}
		}
	}
	let systemLandscape = application.getSystemLandscape();
	if (oFF.notNull(systemLandscape))
	{
		for (let j = 0; j < oFF.SleMetis.s_userSpecifiedSystems.size(); j++)
		{
			let systemDesc = oFF.SleMetis.s_userSpecifiedSystems.get(j);
			systemLandscape.setSystemByDescription(systemDesc);
		}
	}
};
