oFF.SuTaskManager.prototype._getProcRunningTimeStr = function(process)
{
	let runningTimeStr = "-";
	if (oFF.notNull(process))
	{
		let curTimeInMillis = oFF.XSystemUtils.getCurrentTimeInMilliseconds();
		let procTimeMillis = process.getStartTimeInMilliseconds();
		let runningTimeinMillis = curTimeInMillis - procTimeMillis;
		let runningTimeinSeconds = oFF.XLong.convertToInt(runningTimeinMillis / 1000);
		runningTimeStr = oFF.XInteger.convertToString(runningTimeinSeconds);
	}
	return runningTimeStr;
};
oFF.SuTaskManager.prototype._getProcessList = function()
{
	let tmpProcessList = oFF.XList.create();
	let kernel = this.getProcess().getKernel();
	let rootProc = kernel.getChildProcessById(this.m_procId);
	if (oFF.isNull(rootProc))
	{
		rootProc = kernel.getProcess();
	}
	if (oFF.notNull(rootProc))
	{
		tmpProcessList.addAll(rootProc.getChildProcesses());
	}
	let studioProcess = oFF.XCollectionUtils.findFirst(tmpProcessList, (tmpProcess) => {
		return tmpProcess.getProgramCfg() !== null && oFF.XString.isEqual(tmpProcess.getProgramCfg().getName(), "FireflyStudio");
	});
	if (oFF.notNull(studioProcess))
	{
		tmpProcessList.addAll(studioProcess.getChildProcesses());
	}
	return tmpProcessList;
};
